                return allLongsword;
            }
        }

        public byte Level
        {
            get
            {
                return (byte)(_level + 1);
            }
            set
            {
                if (value == 0)
                    _level = 1;
                else
                    _level = (byte)(value - 1);
            }
        }

        public byte Slots
        {
            get
            {
                return _slots;
            }
            set
            {
                if (value < 4)
                {
                    _slots = value;
                }
            }
        }



        public MonHunEquip(byte[] equip)
        {
            _type = equip[0];

            _id = equip[1];
            _res1 = equip[2];
            _level = equip[3];

            _unk1 = new byte[32];
            Array.Copy(equip, 4, _unk1, 0, _unk1.Length);

            _slots = equip[16];
        }

        public static MonHunEquip Create(byte[] equip)
        {
            MonHunEquip obj = null;

            byte tp = equip[0];

            switch (tp)
            {
                case 6:
                    obj = new MonHunTalisman(equip);
                    break;
                default:
                    obj = new MonHunEquip(equip);
                    break;
            }

            return obj;
        }

        public virtual byte[] Serialize()
        {
            byte[] ret = new byte[36];

            ret[0] = _type;
            ret[1] = _id;
            ret[2] = _res1;
            ret[3] = _level;

            Array.Copy(_unk1, 0, ret, 4, _unk1.Length);

            ret[16] = _slots;

            return ret;
        }




