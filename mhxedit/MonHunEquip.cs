            }
        }
        public string[] IDAvailable
        {
            get
            {
                switch (_type)
                {
                    case 0:
                        return allNull;
                    case 1:
                        return allHead;
                    case 2:
                        return allBody;
                    case 3:
                        return allArms;
                    case 4:
                        return allWaist;
                    case 5:
                        return allLegs;
                    case 6:
                        return allTalisman;
                    case 7:
                        return allGreatsword;
                    case 8:
                        return allSwordAndShield;
                    case 9:
                        return allHammer;
                    case 10:
                        return allLance;
                    case 11:
                        return allHeavyBowgun;
                    case 12:
                        return allNone15;
                    case 13:
                        return allLightBowgun;
                    case 14:
                        return allLongsword;
                    case 15:
                        return allGunlance;
                    case 16:
                        return allBow;
                    case 17:
                        return allDualBlades;
                    case 18:
                        return allHuntingHorn;
                    case 19:
                        return allInsectGlaive;
                    case 20:
                        return allChargeBlade;
